<h3>Formatting Instructions</h3>

Please follow these guidelines to produce your camera-ready paper:
<UL>
<LI>Your paper does not exceed 10 pages including references.
<LI>It is in PDF format.
<LI>It has all fonts embedded in the PDF file.
<LI>It uses only scalable font types (like Type 1, TrueType) --- bit-mapped font types (like Type 3) are not acceptable.
<LI>All figures are embedded in vector graphics or bitmaps with a resolution of at least 300 dpi.

<LI>The format of your paper must strictly adhere to the <a href="http://www.acm.org/sigs/publications/proceedings-templates 
">ACM SIG Proceedings Format</a>.
<li>
Make sure you correctly classified your paper using the 
<a href="http://www.acm.org/about/class/1998">ACM Computing Classification categories and terms</a>. (ACM requires the three small sections 'Categories and Subject Descriptors', 'General Terms', and 'Keywords' to follow the abstract. The keywords can be freely chosen, the content of the other two has to be picked from the provided list.)
<li><a href="http://www.acm.org/sigs/publications/sigfaq">ACM-FAQ</a>
</ul>
