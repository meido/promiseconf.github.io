<h2>Initial Submission Instructions</h2>

<p>This section refers to initial submission to the conference.
Hence, for PROMISE'11, it is now out-dated.
</p>

<p>By submitting to PROMISE, the author(s) agree to the following conditions, if their paper is accepted:

<ul>
<li>Submit a final, revised, camera-ready version of the abstract by the deadline specified for camera-ready abstracts,
<li>At least one author must register for the conference.
<li>At least one author must attend the conference to give an oral presentation of their research.
</ul>

<p><b>
STRUCTURED ABSTRACTS:</b>

In order to better facilitate the dissemination of our empirical results, it is a requirement that all PROMISE papers begin with structured abstracts.

<p><b>
FORMAT:</b>
The format for the structured abstracts can be found at 
<a href="http://www.dur.ac.uk/ebse/abstracts.php"> http://www.dur.ac.uk/ebse/abstracts.php</a>.
Papers that do not offer structured abstracts will NOT be reviewed.

<p><b>FORMATTING:</b>

Submissions and final camera-ready versions must follow the ACM SIG Proceedings style. Word, Word Perfect, and LaTex templates can be found here: 
<a href="http://www.acm.org/sigs/publications/proceedings-templates">
http://www.acm.org/sigs/publications/proceedings-templates</a>
The submitted version of your paper must be in Adobe pdf. Please use an available tool to convert your Word or LaTex output to pdf.

