<h2>Review Process</h2>
<ul>
<li>Paper reviews:
<ul>
<li>
The review process was supported by easychair.
<li>After submission, PC members indentified their conflicts of interest (after which, PC members were blocked from viewing papers
and reviews for which they were conflicted).
<li>PC members then bidded on their preferred papers.
<li>Papers were allocated according to those bids. PC members were given one month to review 5 to 8 papers.
<li>Reviews were collected. Each paper was scored 2,1,-1,-2 for "accept!", "accept?", "reject?", or "reject!".
<li>As soon as a paper had 2 or more reviews, the PC chair checked for contradictory reviews. If any where found, an on-line discussion was initiated between reviewers to resolve the contradictions.
<li>At the end of the review period, the PC chair accepted or rejected papers based on the reviews and subsequent discussions.
<li>Reviews and notifications were sent to authors (with no reviewer identified).
</ul>
<li>Best paper review:
<ul>
<li>Based on paper review scores, the PC chair passed three candidates to the PROMISE steering committee.
<li>All steering committee members with conflicts with those papers recused themselves from the deliberations.
<li>The remaining committee members selected the best paper.
</ul>

