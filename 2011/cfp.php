<h2>Call for Papers</h2>

<P>
<b>ABOUT:</b>
The PROMISE conference seeks
verifiable and repeatable models that are useful for the
implementation, evaluation, and management of software processes and projects.
Such models relate to
software development efforts (both in general
or software development for
specific domains such as 
telecom, finance, scientific applications, etc).
<p>
<b>TOPICS:</b> Our topics of interest include but are not limited to:
<ul>
<li>Effort prediction models
<li>Defect prediction models
<li>Meta-analysis and generalizations of predictive models exploring certain questions (e.g., defect prediction)
<li>Replicated studies
<li>Predicting various intermediate or final outcomes of interest regarding business, team, human, people, process, and organizational aspects of software engineering
<li>Privacy and ethical issues in sharing and modeling
<li>Qualitative research guiding and informing the process of building future predictive models
<li>Instance-based models predicting outcomes by examining similarities to earlier experiences
<li>Industrial experience reports detailing the application of software technologies - processes, methods, or tools - and their effectiveness in industrial settings.
<li>Tools for software researchers that effectively gather and analyze data to support reproducible and verifiable research.
</ul>

<p><b>DATA:</b>
PROMISE 2011 will give
the highest priority to empirical studies based on publicly available
datasets. It is therefore encouraged, but it is not mandatory, that conference attendees contribute the
data used in their analysis to the on-line <a href="http://promisedata.org/data">PROMISE data repository</a>.
The repository currently holds
130 data sets, which can be used to repeat/confirm/refute/improve
previous results. 

